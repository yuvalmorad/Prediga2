var round = -1;
var gamesFiltered = [];
var gamesResults = [];
games.matches.forEach(function (match, idx) {
    if (!match["date"]) {
        return;
    }
    round += 1;
    idNum += 1;

    // id
    match["_id"] = id + idNum;

    // teams
    var teams = match["teams"].split(" - ");
    match["team1"] = teams[1]
    match["team2"] = teams[0];
    delete match["teams"];

    // date
    var hour = "20:0";
    if (match["review"] !== "review") {
        hour = match["review"];
    }

    var datesSplit = match["date"].split(".");
    var hourSplit = hour.split(":");
    var d = new Date(20 + datesSplit[2], datesSplit[1] - 1, datesSplit[0], hourSplit[0], hourSplit[1]);
    match["kickofftime"] = d;
    delete match["review"];
    delete match["date"];

    // score
    if (match["score"].includes(":")) {
        match["status"] = "CLOSED_GAME";
        var scoreSplit = match["score"].split(":");
        var team1Goals = scoreSplit[0];
        var team2Goals = scoreSplit[1];
        var winner = 'Draw';
        if (team1Goals > team2Goals) {
            winner = match["team1"];
        } else if (team1Goals < team2Goals) {
            winner = match["team2"];
        }

        var gameResult = {
            matchId: match["_id"],
            winner: winner,
            team1Goals: team1Goals,
            team2Goals: team2Goals,
            goalDiff: Math.abs(team1Goals - team2Goals),
            firstToScore: 'None'
        };
        gamesResults.push(gameResult);
    } else {
        match["status"] = "PRE_GAME";
    }
    delete match["score"];

    match["type"] = "Round " + (Math.floor(round / 7) + 1);
    match["league"] = "IsraeliLeague18";
    gamesFiltered.push(match);
});


